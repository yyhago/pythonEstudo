# Operações com diferentes tipos de dados
print(11 + 10 + 1000)  # Soma de inteiros
print(1.5 + 1 + 0.5)  # Soma de floats
print(True)  # Booleano True
print(False)  # Booleano False
print("Python")  # String simples

# Exemplo de conversões entre tipos
teste_numero = "123"
print(int(teste_numero) + 10)  # Convertendo string para inteiro e somando
print(float(teste_numero) + 0.5)  # Convertendo string para float

# Verificando tipos de dados
print(type(10))  # Tipo inteiro
print(type(10.5))  # Tipo float
print(type("Texto"))  # Tipo string
print(type(True))  # Tipo booleano
